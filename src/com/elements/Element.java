package com.elements;


public abstract class Element {
    protected int vida;
    protected int ataque;
    protected int defensa;
    protected int magia;
    
    
    
    public int getVida() {
		return vida;
	}



	public int getAtaque() {
		return ataque;
	}



	public int getDefensa() {
		return defensa;
	}



	public int getMagia() {
		return magia;
	}
    
}
